        unset($this->session->data['payment_method']);
        unset($this->session->data['payment_methods']);
        unset($this->session->data['guest']);
        unset($this->session->data['comment']);
        unset($this->session->data['order_id']);
        unset($this->session->data['coupon']);
        unset($this->session->data['reward']);
        unset($this->session->data['voucher']);
        unset($this->session->data['vouchers']);
        unset($this->session->data['totals']);

        $this->document->setTitle('Your quote has been sent successfully');

        $data['breadcrumbs'] = array();

        $data['breadcrumbs'][] = array(
            'text' => $this->language->get('text_home'),
            'href' => $this->url->link('common/home')
        );

        $data['heading_title'] = 'Your quote has been sent successfully';
        $data['text_message'] = sprintf('<p>Your quote has been successfully processed!</p><p>Please direct any questions you have to the <a href="%s">store owner</a>.</p><p>Thanks for contacting with us online!</p>', $this->url->link('information/contact'));

        $data['button_continue'] = 'Goto Site';

        $data['continue'] = $this->url->link('common/home');

        $data['column_left'] = $this->load->controller('common/column_left');
        $data['column_right'] = $this->load->controller('common/column_right');
        $data['content_bottom'] = $this->load->controller('common/content_bottom');
        $data['footer'] = $this->load->controller('common/footer');
        $data['header'] = $this->load->controller('common/header');

        if (file_exists(DIR_TEMPLATE . $this->config->get('config_template') . '/template/module/requestquote/thankyou.tpl')) {
            $this->response->setOutput($this->load->view($this->config->get('config_template') . '/template/module/requestquote/thankyou.tpl', $data));
        } else {
            $this->response->setOutput($this->load->view('default/template/module/requestquote/thankyou.tpl', $data));
        }
    }

